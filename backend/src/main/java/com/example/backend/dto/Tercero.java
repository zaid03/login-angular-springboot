package com.example.backend.dto;

public class Tercero {
    private String idenTercero;
    private String NIFtercero;
    private String nomTercero;
    private String apellTercero;

    public String getIdenTercero() { return idenTercero; }
    public void setIdenTercero(String idenTercero) { this.idenTercero = idenTercero; }

    public String getNIFtercero() { return NIFtercero; }
    public void setNIFtercero(String NIFtercero) { this.NIFtercero = NIFtercero; }

    public String getNomTercero() { return nomTercero; }
    public void setNomTercero(String nomTercero) { this.nomTercero = nomTercero; }

    public String getApellTercero() { return apellTercero; }
    public void setApellTercero(String apellTercero) { this.apellTercero = apellTercero; }
}
